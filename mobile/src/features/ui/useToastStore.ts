import { create } from 'zustand'

type ToastType = 'success' | 'error' | 'info'

interface ToastState {
  visible: boolean
  message: string
  type: ToastType
  showToast: (message: string, type?: ToastType) => void
  hideToast: () => void
}

export const useToastStore = create<ToastState>((set) => ({
  visible: false,
  message: '',
  type: 'info',
  showToast: (message, type = 'info') => {
    set({ visible: true, message, type })
    // Auto-hide handled in component or here with timeout, usually component is safer for clear timeouts on unmount
    // But store-based timeout is also common. Let's let the component handle the timer to avoid state updates on unmounted components
  },
  hideToast: () => set({ visible: false }),
}))
